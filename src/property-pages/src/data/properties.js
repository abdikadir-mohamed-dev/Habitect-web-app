export const formatPrice = (property) => {
  const formatted = property.price.toLocaleString("en-US", {
    style: "currency",
    currency: "USD",
    maximumFractionDigits: 0,
  });
  return property.priceUnit ? `${formatted}${property.priceUnit}` : formatted;
};
